									<h2 class="judul">Gunung Semeru</h2>
									<p>Balai Besar Taman Nasional Bromo Tengger Semeru (BB TNBTS) membuka kembali Gunung Semeru untuk jalur pendakian mulai 12 Mei 2019 ini. Seiring pembukaan jalur pendakian itu, ada kebijakan baru yang turut ditetapkan pengelola taman nasional tersebut. Kebijakan itu berupa menaikkan nilai premi asuransi untuk para pengunjung dari semula Rp2.500 menjadi Rp4.000, khusus untuk pengunjung nusantara. Perubahan itu otomatis ikut menaikkan tiket masuk ke kawasan Gunung Semeru dan Bromo. "Itu berdasarkan rapat koordinasi bersama dan mulai diberlakukan pada 1 Juni mendatang. Tiket masuk jadi berubah," kata Kepala BB TNBTS, John Kenedie di Malang, Jumat 10 Mei 2019.</p>
                  <p>Tiket masuk ke Bromo saat week day semula Rp27.500 menjadi Rp29.000. Di hari libur dari Rp32.500 jadi Rp34.000. Untuk tiket masuk Gunung Semeru di hari biasa sebelumnya Rp 17.500 jadi Rp 19.000. Di hari libur sebelumnya Rp 22.500 menjadi Rp 24.000. "Perubahan asuransi dan tiket itu bagi pengunjung nusantara saja, untuk pengunjung mancanegara tetap tidak ada perubahan," ujar John. Perubahan nilai premi asuransi itu mengacu pasal 20 huruf f UU nomor 10 tahun 2009 tentang Kepariwisataan. Serta survei kepuasan pengunjung yang menilai santunan asuransi saat ini masih kurang. Besar santunan untuk pengunjung yang mengalami musibah sampai meninggal dunia ikut berubah. Santunan pengunjung yang meninggal dunia bukan karena kecelakaan dari sebelumnya Rp 20 juta menjadi Rp 25 juta. Pengunjung Gunung Semeru dan Bromo yang meninggal karena kecelakaan santunannya dari Rp 60 juta naik Rp 75 juta. Santunan cacat tetap maupun sebagian karena kecelakaan dari Rp 60 juta jadi Rp 75 juta. Biaya perawatan dari Rp 6 juta jadi Rp 7,5 juta. "Dana asuransi itu juga digunakan mengirim jenasah pengunjung yang kecelakaan hingga meninggal dunia ke daerah asalnya," urai John.</p>
